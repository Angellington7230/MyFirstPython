# Tipos int e float
# int - > Número Inteiro
# O tipo int representa qualquer número
# Positivo ou Negativo. int sem sinl é Positivo
print(11) # int
print(-11) # int
print(0)

# Float -> Número com ponto flutuante
# O tipo floar representa qualquer número Positivo ou Negativo com ponto flutuante.
# Float sem sinal é considerado positivo.
print(1.1, 10.11)
print(0.0, -1.5)


