# Variáveis são usadas para salvar alo na memória do computador.
# PEP8: inicie variáveis com letras minúsculas, pode usar
# números e underline _.
# O sinal de = é o operador de atribuição. Ele é usado para
# atribuir um valor a um nome (variável).
# Uso: nome_variavel = expressão
nome_completo = 'Isabela Aloe Vera'
soma_setenta_mais_dois = 70 + 2
print(nome_completo) # Isabela Aloe Vera
print(soma_setenta_mais_dois)
print(nome_completo, soma_setenta_mais_dois)