"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str - > string - > texto
String são textos dentro de aspas
"""
# aspas simples
print('Minerva Alves')
# aspas duplas
print("Minerva Alves")
# escape
print("Minerva \"Alves") # -> Minerva "Alves
# r
print(r"Minerva \"Alves\"") # -> Minerva \"Alves\"


# Truques Experientes
print("Minerva 'Alves'") # output: Minerva 'Alves'
print('Minerva "Alves"') # output: Minerva "Alves"