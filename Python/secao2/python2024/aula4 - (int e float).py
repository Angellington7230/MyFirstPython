# Tipos int e float
# int -> Número inteiro
# positivo ou negativo. int sem sinal é considerado positivo.

print("1") # output: 1
print(1) # output: 1
print(0) # output: 0
print(-1) #output: -1


#float -> Número com ponto flutuantes
# O tipo float representa qualquer número
# positivo ou negativo com ponto flutuante.
# float sem sinal é considerado positivo.

print(1.1) # output: 1.1
print(-1.2) # output: -1.2
print(0.0) # output: 0.0

# A função type mostra o tipo que o Python inferiu ao valor.
print(type(0)) # output: <class 'int'>
print(type(0.0)) # output: <class 'float'>
print(type('0'))  # output: <class 'str'>